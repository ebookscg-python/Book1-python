# Put the first letter into upper case.
print("the days are short.".capitalize())   
# Center contemplating 20 spaces.
print("Spring".center(20))   
# Center and fill in with * left and right.
print("Spring".center(20,'*'))   
print("Spring".ljust(20))   # Left justify.
# Left justify and fill with * on the right.
print("Spring".ljust(20, '*'))   
print("Spring".rjust(20))   # Right justify.
# Right justify and fill with * on the left.
print("Spring".rjust(20, '*'))   

