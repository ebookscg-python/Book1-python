print("\nHello autumn")  # Drop down a line and print Hello autumn.
print("\\Hello autumn")  # Print\Hello autumn.
print("Greeting: \tHello autumn!")  # Horizontally tab and print Hello autumn.
print("\"Hello autumn\"")  # Print "Hello autumn".
print("\'Hello autumn\'")  # Print 'Hello autumn'.
print("\aHello autumn")  # Print Hello autumn and ring the bell alert sounds.
